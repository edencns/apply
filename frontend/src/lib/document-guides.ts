/* ─── 공개 API ─────────────────────────────────── */

function matches(name: string, keys: string[]): boolean {
  return keys.some((k) => name.includes(k));
}

/**
 * 서류 이름 → 검토 가이드. 매칭되는 가이드가 없으면 null.
 *
 * 매칭 우선순위: 등록 순서. 더 구체적인 키워드(예: "직계존속 출입국")를
 * 먼저 등록해 일반 "출입국"보다 우선 매칭되도록 했음.
 */
export function getDocumentGuide(docName: string): DocumentGuide | null {
  if (!docName) return null;
  for (const { keys, guide } of GUIDES) {
    if (matches(docName, keys)) return guide;
  }
  return null;
}
